public class Comparisons {
    public static void main(String[] args)
    {
        System.out.printf("5 > 10 is a %s statement%n", 5 > 10);
        System.out.printf("5 < 10 is a %s statement%n", 5 < 10);
        System.out.printf("5 <= 10 is a %s statement%n", 5 <= 10);
        System.out.printf("5 >= 10 is a %s statement%n", 5 >= 10);
        System.out.printf("5 == 10 is a %s statement%n", 5 == 10);
        System.out.printf("5 == 5 is a %s statement%n", 5 == 5);
    }
}
